    ctx.register_udf(udfs::STNumPointsUDF::udf());
    ctx.register_udf(udfs::STAsTextUDF::udf());
    ctx.register_udf(udfs::STAsEWKTUDF::udf());
    ctx.register_udf(udfs::STAsGeoJSONUDF::udf());

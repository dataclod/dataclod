    ctx.register_udf(udfs::STGeomFromEWKTUDF::udf());
    ctx.register_udf(udfs::STGeomFromWKBUDF::udf());
    ctx.register_udf(udfs::STGeomFromEWKBUDF::udf());
    ctx.register_udf(udfs::STGeomFromGeoJSONUDF::udf());


    ctx.register_udf(udfs::STBuildAreaUDF::udf());
    ctx.register_udf(udfs::STContainsUDF::udf());
    ctx.register_udf(udfs::STIntersectsUDF::udf());
    ctx.register_udf(udfs::STWithinUDF::udf());
    ctx.register_udf(udfs::STEqualsUDF::udf());
    ctx.register_udf(udfs::STCoversUDF::udf());
    ctx.register_udf(udfs::STCoveredByUDF::udf());
